            <h3>Environment, Climate Change & Resilience</h3>
            <p>Sustainable Development Goal 11 directs nations to make their cities and human settlements inclusive, safe, resilient and sustainable. Mainstreaming resilience into urban development is a global agenda. In India, the increasing pace of urbanization and associated challenges are aggravating the vulnerability of cities to shocks and stresses associated with climate change and disasters. Considering this, NIUA is working towards embedding resilience at the heart of urban discourse. As an institute, working on cities of tomorrow, it understands that urban policies and decisions taken today will have direct impacts on a city’s resilience and capacity to respond to these challenges in future. NIUA is working towards building resilience through the lenses of climate smart cities, resilient infrastructure, urban river system management and capacity building on ecosystem services.</p>
