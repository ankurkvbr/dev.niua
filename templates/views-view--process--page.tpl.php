                <p>Urban governance and finance play a critical role in shaping the physical and social character of urban regions; influence the quantity and quality of local services; determine the sharing of costs and distribution of resources among different groups; affect residents’ ability to engage in decision-making and influence local government accountability. NIUA as an urban policy think tank has actively provided wide-ranging technical assistance to MoHUA, states and ULBs on municipal finance and governance including pan-India mission-mode capacity building for reform adoption with a 4000 ULBs outreach; evaluating municipal health and performance of over 2000 ULBs; and direct hand-holding of a city agency for building its capacities to respond effectively to changing needs and emerging trends.</p>
