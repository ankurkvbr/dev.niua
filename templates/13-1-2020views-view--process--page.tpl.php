<h3>Policy and Planning</h3>
<p>NIUA is working towards ensuring the urban planning and design practice adequately responds to the inherent demographic and economic forces that underpin India’s urbanization. To capture universal principles from global practices, national and local experience that could support the development of diverse planning approaches adapted to different contexts and scales. The emphasis of the institute is on establishing a system of rules and regulations that provide a solid and predictable long-term framework for urban growth and development. We aim to accomplish this by helping the public and private sectors: build capacity for informed, creative decisions; shape public policy; and implement the best community practices.</p>
