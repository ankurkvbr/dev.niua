<h3>Knowledge Management & Capacity Building</h3>
<p>NIUA plays a catalytic role in linking up data, information, and ideas with those in search of development solutions—in ensuring that knowledge for development is readily available to citizens, civil society, opinion makers, researchers, and government policy makers at all levels. Our aim is to ensure that knowledge reaches out to better encompass the experiences of successful practices—not with ordered templates, blueprints, or prescriptions—but with inquiry, innovation, cooperation, and openness.</p>

<p>NIUA strives towards effectively managing the knowledge generated through various projects, providing faster and more effective access to relevant internal and external knowledge to maximize the impact. To ensure that all the knowledge developed at NIUA is readily available for its users, platforms such as SmartNet help peer-to-peer learning for city agencies and focuses on creating a knowledge sharing ecosystem that increases connectivity internally and among key stakeholders, partners and institutions.</p>
